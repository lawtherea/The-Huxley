#include <stdio.h>
#include <string.h>
#include <math.h>
#include <stdlib.h>

int main()
{
    int NUM, um, dois, tres, quatro, cinco, seis, sete, oito, nove;
    scanf("%d", &NUM);
    um = NUM * 1;
    dois = NUM * 2;
    tres = NUM * 3;
    quatro = NUM * 4;
    cinco = NUM * 5;
    seis = NUM * 6;
    sete = NUM * 7;
    oito = NUM * 8;
    nove = NUM * 9;
    printf("%d X 1 = %d\n", NUM, um);
    printf("%d X 2 = %d\n", NUM, dois);
    printf("%d X 3 = %d\n", NUM, tres);
    printf("%d X 4 = %d\n", NUM, quatro);
    printf("%d X 5 = %d\n", NUM, cinco);
    printf("%d X 6 = %d\n", NUM, seis);
    printf("%d X 7 = %d\n", NUM, sete);
    printf("%d X 8 = %d\n", NUM, oito);
    printf("%d X 9 = %d\n", NUM, nove);
    return 0;
}